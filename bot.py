dp = Dispatcher()

@dp.message(Command("start"))
async def cmd_start(message: Message):
    await message.answer(
        "Привет! Я бот с расписанием групп.\n\n"
        "Команды:\n"
        "/start — приветствие и список команд\n"
        "/help — помощь\n"
        "/today — группы на сегодня"
    )

@dp.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(
        "Доступные команды:\n\n"
        "/start — приветствие и список команд\n"
        "/help — описание команд\n"
        "/today — показать группы на текущий день"
    )

@dp.message(Command("today"))
async def cmd_today(message: Message):
    day = datetime.now().weekday()
    groups = SCHEDULE.get(day, [])
    if groups:
        text = "Группы на сегодня:\n\n" + "\n".join(groups)
    else:
        text = "Сегодня групп нет."
    await message.answer(text)

async def main():
    bot = Bot(token=BOT_TOKEN)
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())
